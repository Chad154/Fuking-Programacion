public class intercambio_Dos_Variables {

    public static void main(String[] args) {
        int x,y,c;

        x = 1;

        y = 5;

        System.out.println("Las variables inicales son x = "+x+" y = "+y); 

        c = x;
        x = y;
        y = c;

        System.out.println("Las variables cambidas son: x = "+x+" y = "+y);

        
        


        
    }
    
}
